export const getDailyPiece = () => {
  // Get today's date in MM/DD/YYYY format
  const today = new Intl.DateTimeFormat("en-US", {
    year: "numeric",
    month: "2-digit",
    day: "2-digit",
  }).format(new Date());

  // Use the date string to generate a consistent index for the day
  const dateSum = today
    .split("/")
    .reduce((sum, num) => sum + parseInt(num, 10), 0);
  const pieceIndex = dateSum % musicPieces.length;

  return musicPieces[pieceIndex];
};

// Group pieces by different attributes for study mode
export const getPiecesByComposer = () => {
  const groupedByComposer = {};

  musicPieces.forEach((piece) => {
    if (!groupedByComposer[piece.composer]) {
      groupedByComposer[piece.composer] = [];
    }
    groupedByComposer[piece.composer].push(piece);
  });

  return groupedByComposer;
};

